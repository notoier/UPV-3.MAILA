#Grafoaren sakonerako zeharkatzea: BFS Breadth-first search
def graph_has_a_path(graph, start, end):
    visited_nodes = [start] #Bisitatutako nodoak gordetzeko erabiliko den zerrenda, hau da, tratatuak izan diren nodoak
    lista = [start] #Egindako bidea gordetzeko zerrenda
    
    #Lista hutza ez den bitartean, orduan lista errekorrituko dugu, eta listaren azken nodoa, uneko nodoa bezala hartuko dugu (.pop(0) erabiliz)
    while lista:
        node = lista.pop(0)
        #Ateratako nodoa amaiera bada, orduan True bueltatuko dugu.
        if node == end:
            return True
        
        #Grafoaren matrizean konprobatuko dugu ea jasotako nodoa hurrengoarekin lotuta dagoen ala ez. 1 = lotuta; 0 = ez lotuta
        for next_node in range(0, len(graph[node])):
            
            #Lotuta badago eta oraindik ez badugu tratatu, orduan, bidean eta bisitatutako nodoen zerrendatan sartuko dugu hurrengo nodoa
            if graph[node][next_node] == 1 and not next_node in visited_nodes:
                visited_nodes = visited_nodes + [next_node]
                lista = lista + [next_node]
    #Guztia errekorritu ondoren, ez badugu amaiera aurkitu, orduan False bueltatuko dugu.
    return False

